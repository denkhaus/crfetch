package main

type Normalizer struct {
	etcdClient *etcd.Client
	snapSteps  []uint
}

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////
func (n *Normalizer) normalize(uint ts, uint snap) uint {
	return ts - (ts % snap)
}

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// Init
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////
func (n *Normalizer) Init(client *etcd.Client, snapSteps []uint) {
	n.etcdClient = client
	n.snapSteps = snapSteps
}

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// NewNormalizer
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////

func NewNormalizer() *Normalizer {
	return &Normalizer{}
}
